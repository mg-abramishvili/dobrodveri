<script>
export default {
    data() {
        return {
            reviews: [],

            views: {
                loading: true,
            }
        }
    },
    created() {
        this.loadReviews()
    },
    methods: {
        loadReviews() {
            axios.get('/_admin/reviews')
            .then(response => {
                this.reviews = response.data

                this.views.loading = false
            })
        },
        publish(id) {
            axios.put(`/_admin/review/${id}/update`)
            .then(response => {
                this.loadReviews()
            })
        },
        del(id) {
            if (confirm("Точно удалить отзыв?")) {
                axios.delete(`/_admin/review/${id}/delete`)
                .then(response => {
                    this.loadReviews()
                })
            }
        },
    },
}
</script>